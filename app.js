// Floating background symbols (decorative canvas)
// -----------------------------------------------------------------
(function initFloatingSymbols() {
  const canvas = document.getElementById('heroCanvas');
  if (!canvas) return;
  const ctx = canvas.getContext('2d');
  const SYMBOLS = ['{ }', '< />', '1C', 'AI', 'LLM', '()', '=>', '//', 'MCP', 'CFE'];

  let particles = [];

  function resize() {
    canvas.width = canvas.offsetWidth * devicePixelRatio;
    canvas.height = canvas.offsetHeight * devicePixelRatio;
    ctx.setTransform(1, 0, 0, 1, 0, 0);
    ctx.scale(devicePixelRatio, devicePixelRatio);
  }

  function createParticles() {
    const area = canvas.offsetWidth * canvas.offsetHeight;
    const count = Math.min(Math.floor(area / 25000), 60);
    particles = Array.from({ length: count }, () => {
      const angle = Math.random() * Math.PI * 2;
      const speed = 0.15 + Math.random() * 0.35;
      return {
        x: Math.random() * canvas.offsetWidth,
        y: Math.random() * canvas.offsetHeight,
        symbol: SYMBOLS[Math.floor(Math.random() * SYMBOLS.length)],
        size: 10 + Math.random() * 12,
        vx: Math.cos(angle) * speed,
        vy: Math.sin(angle) * speed,
        targetVx: Math.cos(angle) * speed,
        targetVy: Math.sin(angle) * speed,
        opacity: 0.04 + Math.random() * 0.08,
        timer: 0,
        interval: 180 + Math.random() * 300,
      };
    });
  }

  function step() {
    ctx.clearRect(0, 0, canvas.offsetWidth, canvas.offsetHeight);
    particles.forEach((p) => {
      ctx.font = `${p.size}px Geist, monospace`;
      ctx.fillStyle = `rgba(255,255,255,${p.opacity})`;
      ctx.fillText(p.symbol, p.x, p.y);

      if (++p.timer >= p.interval) {
        p.timer = 0;
        p.interval = 180 + Math.random() * 300;
        const angle = Math.random() * Math.PI * 2;
        const speed = 0.15 + Math.random() * 0.35;
        p.targetVx = Math.cos(angle) * speed;
        p.targetVy = Math.sin(angle) * speed;
      }

      p.vx += (p.targetVx - p.vx) * 0.02;
      p.vy += (p.targetVy - p.vy) * 0.02;
      p.x += p.vx;
      p.y += p.vy;

      if (p.x < -80) p.x = canvas.offsetWidth + 80;
      if (p.x > canvas.offsetWidth + 80) p.x = -80;
      if (p.y < -40) p.y = canvas.offsetHeight + 40;
      if (p.y > canvas.offsetHeight + 40) p.y = -40;
    });
    requestAnimationFrame(step);
  }

  resize();
  createParticles();
  step();
  window.addEventListener('resize', () => { resize(); createParticles(); });
})();

