const lottieInstances = [];

function destroyLottieInstances() {
  lottieInstances.forEach((anim) => {
    try { anim.destroy(); } catch (_) { /* already gone */ }
  });
  lottieInstances.length = 0;
}

function renderFeatures() {
  const grid = el.featuresGrid;
  grid.innerHTML = '';
  destroyLottieInstances();

  i18n[lang].features.forEach((feature, i) => {
    const card = document.createElement('div');
    card.className = 'card';
    card.style.animationDelay = `${i * 100}ms`;

    const iconWrap = document.createElement('div');
    iconWrap.className = 'card-icon';
    const iconTarget = document.createElement('div');
    iconTarget.id = `lottie-${i}`;
    iconWrap.appendChild(iconTarget);

    const title = document.createElement('h3');
    title.textContent = feature.title;

    const desc = document.createElement('p');
    desc.textContent = feature.desc;

    card.append(iconWrap, title, desc);
    grid.appendChild(card);
  });

  // Lottie needs the containers to exist in the DOM first.
  requestAnimationFrame(() => {
    i18n[lang].features.forEach((feature, i) => {
      const target = document.getElementById(`lottie-${i}`);
      const data = window.ANIM_DATA && window.ANIM_DATA[feature.icon];
      if (!target || !data || !data.v) return;
      const anim = window.lottie.loadAnimation({
        container: target,
        renderer: 'svg',
        loop: true,
        autoplay: true,
        animationData: data,
      });
      lottieInstances.push(anim);
    });
  });
}

function renderInstall() {
  const list = el.installSteps;
  list.innerHTML = '';
  i18n[lang].install.forEach((stepText, i) => {
    const step = document.createElement('div');
    step.className = 'step';
    step.style.animationDelay = `${i * 100}ms`;

    const num = document.createElement('span');
    num.className = 'step-num';
    num.textContent = String(i + 1);

    const text = document.createElement('span');
    text.className = 'step-text';
    text.textContent = stepText;

    step.append(num, text);
    list.appendChild(step);
  });
}

function applyLang() {
  document.documentElement.lang = lang;
  el.langBtn.textContent = lang === 'ru' ? 'EN' : 'RU';

  document.querySelectorAll('[data-i18n]').forEach((node) => {
    node.textContent = t(node.dataset.i18n);
  });
  document.querySelectorAll('[data-i18n-placeholder]').forEach((node) => {
    node.placeholder = t(node.dataset.i18nPlaceholder);
  });

  renderFeatures();
  renderInstall();

  if (chatHistory.length === 0) {
    chatHistory = [{ role: 'assistant', content: t('chat_welcome') }];
  }
  renderMessages();
}

el.langBtn.addEventListener('click', () => {
  lang = lang === 'ru' ? 'en' : 'ru';
  localStorage.setItem('lang', lang);
  applyLang();
});



