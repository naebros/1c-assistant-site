// Chat: rendering messages
// -----------------------------------------------------------------
function renderMessages() {
  const container = el.chatMessages;
  container.innerHTML = '';

  chatHistory.forEach((message, i) => {
    const isUser = message.role === 'user';
    const isLatestReply = i === chatHistory.length - 1 && message.role === 'assistant' && chatHistory.length > 1;

    const row = document.createElement('div');
    row.className = `msg-row ${isUser ? 'user' : 'assistant'}${isLatestReply ? ' msg-new' : ''}`;

    const bubble = document.createElement('div');
    bubble.className = 'msg-bubble';
    bubble.innerHTML = parseMarkdown(message.content);

    row.appendChild(bubble);
    container.appendChild(row);
  });

  container.scrollTop = container.scrollHeight;
}

function showTypingIndicator() {
  const row = document.createElement('div');
  row.className = 'msg-row assistant msg-new';
  row.id = 'typingIndicator';
  row.innerHTML = `
    <div class="typing-bubble">
      <span class="typing-dot"></span>
      <span class="typing-dot"></span>
      <span class="typing-dot"></span>
    </div>`;
  el.chatMessages.appendChild(row);
  el.chatMessages.scrollTop = el.chatMessages.scrollHeight;
}

function hideTypingIndicator() {
  document.getElementById('typingIndicator')?.remove();
}

// -----------------------------------------------------------------
// Chat: sending messages
// -----------------------------------------------------------------
function setSending(isSending) {
  el.sendBtn.disabled = isSending;
  el.chatInput.disabled = isSending;
}

async function sendMessage() {
  const text = el.chatInput.value.trim();
  if (!text) return;

  chatHistory.push({ role: 'user', content: text });
  el.chatInput.value = '';
  renderMessages();
  showTypingIndicator();
  setSending(true);

  try {
    const response = await fetch(CHAT_ENDPOINT, {
      method: 'POST',
      headers: { 'Content-Type': 'application/json' },
      body: JSON.stringify({
        messages: chatHistory.slice(-MAX_HISTORY_MESSAGES),
        lang,
      }),
    });

    if (!response.ok) {
      throw new Error(`Server responded with ${response.status}`);
    }

    const data = await response.json();
    const reply = data.reply || t('chat_error');
    chatHistory.push({ role: 'assistant', content: reply });
  } catch (err) {
    console.error('Chat request failed:', err);
    chatHistory.push({ role: 'assistant', content: t('chat_error') });
  } finally {
    hideTypingIndicator();
    setSending(false);
    renderMessages();
    el.chatInput.focus();
  }
}


el.chatInput.addEventListener('keydown', (event) => {
  if (event.key === 'Enter' && !event.shiftKey) {
    event.preventDefault();
    sendMessage();
  }
});
el.sendBtn.addEventListener('click', sendMessage);

