// Chat: markdown rendering (safe — escapes HTML before applying rules)
// -----------------------------------------------------------------
let codeBlockCounter = 0;

function escapeHtml(str) {
  return str
    .replace(/&/g, '&amp;')
    .replace(/</g, '&lt;')
    .replace(/>/g, '&gt;');
}

function parseMarkdown(raw) {
  if (!raw) return '';

  let text = escapeHtml(raw);
  codeBlockCounter = 0;

  // Fenced code blocks
  text = text.replace(/```(\w*)\n?([\s\S]*?)```/g, (_match, language, code) => {
    const id = `code-${codeBlockCounter++}`;
    const label = language ? `<span class="code-lang">${escapeHtml(language)}</span>` : '<span></span>';
    return (
      `<div class="code-block">` +
      `<div class="code-block-header">${label}` +
      `<button class="copy-btn" type="button" data-copy-target="${id}">⧉</button>` +
      `</div><pre><code id="${id}">${code.trim()}</code></pre></div>`
    );
  });

  // Inline code
  text = text.replace(/`([^`]+)`/g, '<code>$1</code>');

  // Headings
  text = text.replace(/^### (.+)$/gm, '<h3>$1</h3>');
  text = text.replace(/^## (.+)$/gm, '<h2>$1</h2>');
  text = text.replace(/^# (.+)$/gm, '<h1>$1</h1>');

  // Bold / italic
  text = text.replace(/\*\*(.+?)\*\*/g, '<strong>$1</strong>');
  text = text.replace(/\*(.+?)\*/g, '<em>$1</em>');

  // Lists
  text = text.replace(/^- (.+)$/gm, '<li>$1</li>');
  text = text.replace(/(<li>.*<\/li>\n?)+/g, (match) => `<ul>${match}</ul>`);

  // Links — http(s) only, already-escaped text so this is safe as an href
  text = text.replace(/(https?:\/\/[^\s<]+)/g, '<a href="$1" target="_blank" rel="noopener noreferrer">$1</a>');

  text = text.replace(/\n/g, '<br>');
  return text;
}

// Event delegation for copy buttons — works for all messages, current and future.
el.chatMessages.addEventListener('click', (event) => {
  const btn = event.target.closest('.copy-btn');
  if (!btn) return;
  const targetId = btn.dataset.copyTarget;
  const codeNode = document.getElementById(targetId);
  if (!codeNode) return;

  navigator.clipboard.writeText(codeNode.textContent).then(() => {
    btn.textContent = '✓';
    setTimeout(() => { btn.textContent = '⧉'; }, 1500);
  });
});

